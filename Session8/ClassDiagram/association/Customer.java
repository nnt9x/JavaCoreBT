package association;

/*
Association in terms of objects refers to "has a" relationship between two related objects.
 */
public class Customer {
    private String name;
    private int age;
    private boolean gender;
    private Address address;

    public Customer(){
        super();
    }

    public Customer(String name, int age, boolean gender, Address address) {
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.address = address;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public boolean isGender() {
        return gender;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public Address getAddress() {
        return address;
    }

    public void setAddress(Address address) {
        this.address = address;
    }
}
